            Al día siguiente, antes que saliera el sol, el buen hombre se levantó  al sentir que su mujer dejaba la habitación. Por el pasillo vio cómo ella  caminaba hacia la cocina. La siguió en silencio y cuál no sería su sorpresa al ver, a través del humo del fogón, que su esposa, de espaldas a él, empezó a quitarse la piel y a convertirse en una bola de fuego que salió por la ventana abierta.<br><br>
            El hombre quedó impactado, pero tomó una decisión: recogió la piel de la que él creía su mujer y la quemó en el fogón. Minutos antes del amanecer, la bola de fuego entró a la cocina. Al no encontrar su forma humana, emitió un grito terrible, al mismo tiempo que  se disipó en el aire, dejando como único testigo al joven esposo que, para ese momento, había encanecido completamente del susto. Se dice que el joven nunca recuperó del todo la razón, pero que, a quien se lo solicitaba y le invitaba una copa de vino, le contaba esta historia a detalle.
