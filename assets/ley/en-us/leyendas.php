            Al paso de algunos meses le preguntó: “Querida, tú cocinas muy rico, pero ¿por qué siempre desayunamos moronga?”. Su mujer, sonriente, le contestó: “Recuerda que mi padre es dueño del rastro y lo que no  vende nos lo reparte a sus hijos: a mi hermano mayor le tocan las vísceras, a mi hermana las patas y a mí la sangre”.<br><br>
